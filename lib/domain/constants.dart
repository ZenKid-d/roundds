/// Общие константы домена: границы длительности «муз. трека», срок жизни
/// потока по умолчанию, размеры буферов. Раньше эти числа дублировались в
/// каждом источнике и в движке рекомендаций (45 сек / 720 сек / 30 минут),
/// из-за чего правка в одном месте не подхватывалась в остальных.
///
/// Здесь — единый источник правды, импортируемый источниками и recs.
library;

/// Минимальная длительность музыкального трека в секундах.
/// Ниже — шортсы, клипы, обрывки; не считаем за полноценный трек.
const int minTrackSec = 45;

/// Максимальная длительность музыкального трека в секундах (12 минут).
/// Выше — миксы, стримы, подкасты, концерты целиком.
const int maxTrackSec = 12 * 60; // 720

/// Срок жизни подписанной ссылки потока по умолчанию.
///
/// Реальный срок у каждого сервиса свой и часто не сообщается в ответе,
/// поэтому берём консервативные 30 минут: этого достаточно для запуска трека,
/// а протухшую ссылку плеер всё равно перерезолвит (auto-re-resolve в
/// audio_handler). Источники могут переопределить при наличии точных данных.
const Duration defaultStreamExpiry = Duration(minutes: 30);
